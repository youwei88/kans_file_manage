package pojo;

public class FileInfoBack {
	
	private String fileName;//文件名称(标准格式例如: 10000056_研发信息_20170621.doc)
	private String path;//目录(标准格式例如: /50700361/研发文件/规格文件)
	private String uploadTime;//最后更新时间
	private String fileSize;//文件大小(单位为KB)
	private String author;//创建者
	private String isDeleted;//逻辑删除标记，1为逻辑删除
	private String oaId;//附件ID(OA)
	private String type;//文件类型(OA)
	private String requestId;//流程ID
	private String channelId;//0:文档上传文件,1:OA对接上传
	private String code;//物料编码
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public String getPath() {
		return path;
	}
	public void setPath(String path) {
		this.path = path;
	}
	public String getUploadTime() {
		return uploadTime;
	}
	public void setUploadTime(String uploadTime) {
		this.uploadTime = uploadTime;
	}
	public String getFileSize() {
		return fileSize;
	}
	public void setFileSize(String fileSize) {
		this.fileSize = fileSize;
	}
	public String getAuthor() {
		return author;
	}
	public void setAuthor(String author) {
		this.author = author;
	}
	public String getIsDeleted() {
		return isDeleted;
	}
	public void setIsDeleted(String isDeleted) {
		this.isDeleted = isDeleted;
	}
	public String getOaId() {
		return oaId;
	}
	public void setOaId(String oaId) {
		this.oaId = oaId;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getRequestId() {
		return requestId;
	}
	public void setRequestId(String requestId) {
		this.requestId = requestId;
	}
	public String getChannelId() {
		return channelId;
	}
	public void setChannelId(String channelId) {
		this.channelId = channelId;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	
	
	
	


}
